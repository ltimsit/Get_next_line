#include "libft.h"

int				ft_atoi(const char *str)
{
	int i;
	int total;

	total = 0;
	i = 1;
	while (*str == ' ' || ((*str >= 8) && (*str <= 13)))
		str++;
	if ((*str == '-' || *str == '+') && *(str + 1) >= '0' && *(str + 1) <= '9')
		i = 44 - *str++;
	while ((*str >= '0') && (*str <= '9'))
		total = total * 10 + (*str++ - '0');
	return (total * i);
}
